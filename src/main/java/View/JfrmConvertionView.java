        String currency = cbxCurrencyCode.getSelectedItem().toString();
        int currencyLength = currency.indexOf("(");
        String codeCurrency = currency.substring(currencyLength + 1, currency.length() - 1);

        CurrencyRequest request = new CurrencyRequest(currencyCode, codeCurrency, money);
        CurrencyController controller = new CurrencyController();
        lblMontoConvertido.setText("" + controller.convertCurrency(request).money());



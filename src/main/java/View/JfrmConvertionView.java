    // Obtenemos y mostramos los nombres y la moneda de los paises correspondientes
    private void getCurrencyCode() {

        Set<Currency> c = Currency.getAvailableCurrencies();
        c.stream()
                .filter(currency -> !currency.getDisplayName().contains("("))
                .sorted((c1, c2) -> c1.getDisplayName().compareTo(c2.getDisplayName())) // Ordenar por nombre de visualizaciÃ³n
                .forEach(
                        curren -> cbxCurrencyCode.addItem(curren.getDisplayName().toUpperCase() + " (" + curren.getCurrencyCode().toUpperCase() + ")"));

    }

    private void showImgCountry() {

        CountryController cc = new CountryController();
        FlagDtoResponse fg = cc.getFlag();
        try {
            // Usar HttpClient para descargar la imagen como InputStream
            HttpClient client = HttpClient.newHttpClient();
            HttpRequest request = HttpRequest.newBuilder()
                    .uri(URI.create(fg.png()))
                    .build();

            HttpResponse<InputStream> response = client.send(request, HttpResponse.BodyHandlers.ofInputStream());
            InputStream imageStream = response.body();

            // Leer la imagen desde el InputStream
            Image image = ImageIO.read(imageStream);

            if (image != null) {
                // Redimensionar la imagen para que encaje en el JLabel
                Image scaledImage = image.getScaledInstance(lblImgCountry.getWidth(), lblImgCountry.getHeight(), Image.SCALE_SMOOTH);

                // Establecer la imagen en el JLabel
                lblImgCountry.setIcon(new ImageIcon(scaledImage));

                Locale peruLocale = Locale.of("es", fg.cca2());
                Currency currency = Currency.getInstance(peruLocale);
                currencyCode = currency.getCurrencyCode();
                lblMonedaCountry.setText(currency.getSymbol(peruLocale));
            } else {
                lblImgCountry.setText("No se pudo cargar la imagen");
            }

            // Cerrar el InputStream
            imageStream.close();
        } catch (IOException | InterruptedException e) {
            lblImgCountry.setText("Error al descargar la imagen");
        }

    }

